namespace Klipper\Bundle\FunctionalTestBundle\DependencyInjection;

use Symfony\Component\Config\FileLocator;
use Symfony\Component\DependencyInjection\ContainerBuilder;
use Symfony\Component\DependencyInjection\Extension\Extension;
use Symfony\Component\DependencyInjection\Loader\XmlFileLoader;
use Symfony\Component\Validator\Validator\ValidatorInterface;

/**
 * This is the class that loads and manages your bundle configuration.
 *
 * @author François Pluchino <francois.pluchino@klipper.dev>
 */
class KlipperFunctionalTestExtension extends Extension
{
    /**
     * {@inheritdoc}
     *
     * @throws \Exception
     */
    public function load(array $configs, ContainerBuilder $container): void
    {
        $config = $this->processConfiguration(new Configuration(), $configs);

        $loader = new XmlFileLoader($container, new FileLocator(__DIR__.'/../Resources/config'));
        $loader->load('functional_test.xml');
        $loader->load('command.xml');

        if (interface_exists(ValidatorInterface::class)) {
            $loader->load('validator.xml');
        }

        $container->setParameter('klipper_functional_test.cache_db', $config['cache_db']);
        $container->setParameter('klipper_functional_test.authentication', $config['authentication']);
        $container->setParameter('klipper_functional_test.db_extensions', $config['database_extensions']);
    }
}
